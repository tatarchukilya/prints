#include "libft.h"

int		ft_atoi(const char *str)
{
	unsigned long long int	n;
	size_t					i;
	size_t					is_neg;

	n = 0;
	i = 0;
	is_neg = 0;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		is_neg = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		n = 10 * n + (str[i] - '0');
		i++;
	}
	if (n > 9223372036854775807)
		return (is_neg ? 0 : -1);
	return (is_neg ? -n : n);
}
